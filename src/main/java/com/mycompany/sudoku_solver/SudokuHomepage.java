    private void changeSudokuState(JButton button)
    {    
        if(button == resetButton)
        {
            for(int i=0;i<buttonGrid.length;i++)
            {
                for(int j=0;j<buttonGrid[0].length;j++)
                {
                   JButton currButton = buttonGrid[i][j];
                   if(! predefinedButtons.contains(currButton))
                   {
                       currButton.setText("");
                       currButton.setBackground(Color.WHITE);
                   }
                }
            }
        }
        
        else if(button == solutionButton)
        {
            for(int i=0;i<buttonGrid.length;i++)
             {
                for(int j=0;j<buttonGrid[0].length;j++)
                {
                   JButton currButton = buttonGrid[i][j];
                   if(! predefinedButtons.contains(currButton))
                   {
                       currButton.setText(sudokuGrid[i][j]);
                       currButton.setBackground(new Color(204,204,0));
                   }
                }
             }
        }
        
        else if(button == checkMovesButton)
        {
            for(int i=0;i<buttonGrid.length;i++)
             {
                for(int j=0;j<buttonGrid[0].length;j++)
                {
                   JButton currButton = buttonGrid[i][j];
                   if(! predefinedButtons.contains(currButton) && !buttonGrid[i][j].getText().isEmpty())
                   {
                       if(currButton.getText().equals(sudokuGrid[i][j]))
                          currButton.setBackground(Color.GREEN);
                       
                       else
                           currButton.setBackground(Color.RED);
                   }
                }
             }
        }
        
        
    }
    
    private void copyCurrentState()
    {
        for(int i=0;i<buttonGrid.length;i++)
        {
            for(int j=0;j<buttonGrid[0].length;j++)
            {
                currStateGrid[i][j] = buttonGrid[i][j].getText();
            }
        }
    }
    
    private void revertToOriginalGrid()
    {
        for(int i=0;i<buttonGrid.length;i++)
        {
            for(int j=0;j<buttonGrid[0].length;j++)
            {
                buttonGrid[i][j].setText(currStateGrid[i][j]);
                if(currStateGrid[i][j] == "")
                    buttonGrid[i][j].setBackground(Color.white);
                else if(predefinedButtons.contains(buttonGrid[i][j]))
                    buttonGrid[i][j].setBackground(predefButtonColor);
                else
                    buttonGrid[i][j].setBackground(filledBoxColor);
            }
        }
    }
   
